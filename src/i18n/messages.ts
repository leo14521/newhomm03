const catalogs: Record<Locale, Catalog> = { ko, en, ja, zh, ru };

function resolvePath(catalog: Catalog, parts: string[]): string | undefined {
  let cur: unknown = catalog;
  for (const p of parts) {
    if (cur && typeof cur === "object" && p in (cur as object)) {
      cur = (cur as Record<string, unknown>)[p];
    } else {
      return undefined;
    }
  }
  return typeof cur === "string" ? cur : undefined;
}

const fallbackChain: Locale[] = ["en", "ko"];

export function getMessage(locale: Locale, path: string): string {
  const parts = path.split(".").filter(Boolean);
  const primary = resolvePath(catalogs[locale], parts);
  if (primary !== undefined) return primary;
  for (const fb of fallbackChain) {
    if (fb === locale) continue;
    const s = resolvePath(catalogs[fb], parts);
    if (s !== undefined) return s;
  }
  return path;
}
